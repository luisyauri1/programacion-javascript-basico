// Operadores de Asignación
/**
 * x = x + y -> Suma números
 * x = x - y -> Resta numeros
 * x = x * y -> Multiplica números
 * x = x / y -> Divide numeros
 * x = x % y -> Divide numeros, y luego brinda el residuo.
 * x = x ** y -> Exponenciación
 * x = y -> Asignar valor a la variable
 */

const asigAdd = 10 + 10;
console.log({ asigAdd });

const asigSubtract = 10 - 10;
console.log({ asigSubtract });

const asigMultiply = 10 * 10;
console.log({ asigMultiply });

const asigDivide = 10 / 10;
console.log({ asigDivide });

const asigResidue = 10 % 10;
console.log({ asigDivide });

const asigExpo = 10 ** 10;
console.log({ asigExpo });

const currentValue = 10;
console.log({ currentValue });

// Operadores de aritméticos
/**
 * (+ , - , * , / ) -> Operadores aritméticos estandar
 * % -> Residuo
 * ++ -> Incremento
 * -- -> Decremento
 * - -> Negación Unaria
 * + -> Positivo Unario
 * ** -> Operador de exponenciación
 */

let aritResiude = 20 % 5;
console.log(aritResiude);

let aritIncr = 11;
console.log(++aritIncr);

let aritDecr = 11;
console.log(--aritDecr);

let aritNega = -20;
console.log(aritNega);

let aritPosi = +20;
console.log(aritPosi);

let aritExpo = 20 ** 2;
console.log(aritExpo);
